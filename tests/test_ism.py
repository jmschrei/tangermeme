def test_saturation_mutagenesis_sum_model(X):
	model = SumModel()
	y0, y_hat = saturation_mutagenesis(model, X, device='cpu')

	assert y0.shape == (2, 4)
	assert y_hat.shape == (2, 4, 10, 4)

	assert_array_almost_equal(y0, [[2, 2, 0, 6], [4, 2, 3, 1]])
	assert_array_almost_equal(y_hat[:, :, :3], [
		[[[2, 2, 0, 6],
          [3, 2, 0, 5],
          [3, 1, 0, 6]],

         [[1, 3, 0, 6],
          [2, 3, 0, 5],
          [2, 2, 0, 6]],

         [[1, 2, 1, 6],
          [2, 2, 1, 5],
          [2, 1, 1, 6]],

         [[1, 2, 0, 7],
          [2, 2, 0, 6],
          [2, 1, 0, 7]]],


        [[[5, 1, 3, 1],
          [5, 2, 2, 1],
          [4, 2, 3, 1]],

         [[4, 2, 3, 1],
          [4, 3, 2, 1],
          [3, 3, 3, 1]],

         [[4, 1, 4, 1],
          [4, 2, 3, 1],
          [3, 2, 4, 1]],

         [[4, 1, 3, 2],
          [4, 2, 2, 2],
          [3, 2, 3, 2]]]])

